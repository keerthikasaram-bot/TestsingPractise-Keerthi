public class Ifclass {
  public static void main(String[] args) {
    int x = 20;
    int y = 20;
    if (x == y) {
      System.out.println("x is equal to y");
      
      //Else Condition
      
      int time = 9;
      if (time < 12) {
        System.out.println("Good day.");
      } else {
        System.out.println("Good evening.");
        
        //OUTPUT  x is equal to y
        // Good day.
      }
    }  
  }
}
